  @nonobjc class var textFieldCorner: UIColor {
    return UIColor(white: 200.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var warmGrey: UIColor {
    return UIColor(white: 112.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var darkSkyBlue: UIColor {
    return UIColor(red: 56.0 / 255.0, green: 115.0 / 255.0, blue: 227.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var brownishGrey: UIColor {
    return UIColor(white: 102.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var white: UIColor {
    return UIColor(white: 243.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var greyish: UIColor {
    return UIColor(white: 175.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var whiteTwo: UIColor {
    return UIColor(white: 239.0 / 255.0, alpha: 1.0)
  }
}
